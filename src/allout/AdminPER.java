    private void T3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_T3ActionPerformed
        // TODO add your handling code here:
        try {
            String selected_class = T3.getSelectedItem().toString();
            if(selected_class.length()>0) {
                try {
                    Connection c=DriverManager.getConnection(host, username,password);
                    Statement s=c.createStatement();
                    String query="SELECT name, mobile FROM instructorinfo WHERE class = "+Integer.parseInt(selected_class)+"";
                    ResultSet r=s.executeQuery(query);
                    while(r.next()) {
                        T4.setText(r.getString("name"));
                        T5.setText(r.getString("mobile"));
                    }
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(null,e);
                }
                try {
                    Connection c=DriverManager.getConnection(host, username,password);
                    Statement s=c.createStatement();
                    String query="SELECT fees FROM classfees WHERE class = "+Integer.parseInt(selected_class)+"";
                    ResultSet r=s.executeQuery(query);
                    while(r.next()) {
                        T2.setText(r.getString("fees"));
                    }
                } catch(Exception e) {
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        } catch(NullPointerException e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_T3ActionPerformed
                                 

    
