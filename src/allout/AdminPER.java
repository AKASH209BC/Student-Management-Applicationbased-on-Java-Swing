private void getdata()
    {
        DefaultTableModel Model=(DefaultTableModel) TB.getModel();
        hidden.setVisible(false);
        hidden2.setVisible(false);
        Model.setRowCount(0);
        try
        {
            Connection con=DriverManager.getConnection(host,username,password);
            Statement s=con.createStatement();
            String q="Select * from performance";
            ResultSet r=s.executeQuery(q);
            while(r.next())
            {
                Model.addRow(new Object[]{r.getInt("sid"),r.getInt("fees"),r.getInt("class"),r.getString("iname"),r.getString("imobile"),r.getInt("marks")});
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }

    }
private int check()
    {   int flag=1;
        if(T1.getText().equals(""))
        {    JOptionPane.showMessageDialog(null,"Student ID field can't be empty!!");
             flag=0;  
        }
        else if(T2.getText().equals(""))
        {    
            JOptionPane.showMessageDialog(null,"Fees can't be empty!!");
            flag=0;
        }
        else if(T3.getSelectedItem()==null || T3.getSelectedItem().toString().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Class field can't be empty!!");
            flag=0;
        }
        else if(T4.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Instructor Name field can't be empty!!");
            flag=0;
        }
        else if(T5.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Instructor Mobile field can't be empty!!");
            flag=0;
        }
        Boolean b1=Pattern.compile("[0-9]{4}").matcher(T1.getText()).matches();
        if(b1==false)
        {
            JOptionPane.showMessageDialog(null,"Fill the Student ID correctly!!");
            flag=0;
        }
        if(Integer.parseInt(T2.getText())<500 || Integer.parseInt(T2.getText())>3300)
        {
            JOptionPane.showMessageDialog(null,"Fill the Fees Correctly!!");
            flag=0;
        }
        if(Integer.parseInt(T3.getSelectedItem().toString())<1 ||Integer.parseInt(T3.getSelectedItem().toString())>12)
        {
            JOptionPane.showMessageDialog(null,"Fill the Class correctly!!");
            flag=0;
        }
        Pattern p1=Pattern.compile("[a-zA-Z]{2,20}");
        Matcher m=p1.matcher(T4.getText());
        Boolean b=m.matches();
        if(b==false)
        {
            JOptionPane.showMessageDialog(null,"Fill the name correctly!!");
            flag=0;
        }
        Boolean b3=Pattern.matches("[6789]{1}[0-9]{9}",T5.getText());
        if(b3==false)
        {
            JOptionPane.showMessageDialog(null,"Fill the mobile number correctly!!");
            flag=0;
        }
        if(flag==0)
            return 0;
        else
            return 1;
    }
